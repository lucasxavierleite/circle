    std::string get_random_nickname() {
        if (nicknames.empty()) {
            build_nicknames();
        }

        int fni = rand() % nicknames.size();
        int sni = rand() % nicknames.size();
        int rn = rand() % 10000;

        return nicknames[fni] + nicknames[sni] + std::to_string(rn);
    }

    bool validate_nickname(const std::string &_nickname) {
        if (_nickname.length() > NICKNAME_MAX_CHARACTERS) return false;
        for (auto c : _nickname)
            if (!isascii(c)) return false;

        return true;
    }

